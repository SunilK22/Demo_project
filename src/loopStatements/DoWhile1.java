package loopStatements;

public class DoWhile1 {

	public static void main(String[] args) 
	{
		
		// print table of 3 
		
		int i=3;
		
		do 
		{
			System.out.println(i);
			i=i+3;
		}while (i<=30);
		
		System.out.println("----------");
		
		int j=30;
		
		do
		{
			System.out.println(j);
			j=j-3;
		}while (j>=3);
			

	}

}
