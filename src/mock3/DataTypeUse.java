package mock3;

public class DataTypeUse {

	public static void main(String[] args) 
	{
		int a=10;
		double b=10.5;
		char c='S';
		float d=10.235f;
		boolean e=true;
		String f="Welcome";
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
	}

}
